/**
 * @brief Функция, превращающая число в строку его десятичной записи
 * @param [in] value Число, которое надо превратить
 * @return Указатель на динамическую память, где храниться строка
*/
// --------------------------------------------------------------------------------------------------
char* int_to_char (int value)
{
    int i = 1;
    unsigned int len = 1;

    while (value % i != 0)
    {
        i *= 10;
        len++;
    }

    char* buffer = (char*) calloc (len + 1, sizeof (char));
    if (buffer == NULL)
    {
        EXIT_FUNC("NULL calloc", NULL);
    }

    unsigned int count = 0;
    for (i = 1; count < len; i *= 10, count++)
    {
        buffer[count] = char ('0' + (value % (i * 10)) / i);
    }

    buffer[len] = '\0';

    return buffer;
}
