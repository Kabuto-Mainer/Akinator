/**
 * @brief Хеш-функция
 * @param [in] string Строка, которая преобразуется в хеш
 * @return Хеш строки
*/
// --------------------------------------------------------------------------------------------------
int get_hash (const char* string)
{
    assert (string);

    int index = 0;
    int hash = 5381;
    while (string[index] != '\0')
    {
        hash = int ((size_t) hash * 33 + (size_t) string[index]);
        index++;
    }

    return hash;
}
